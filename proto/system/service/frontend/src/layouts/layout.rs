//------------------------------------------------------------------------------
//! Layout.
//------------------------------------------------------------------------------

use super::{ Header, Footer };

use sycamore::prelude::*;


//------------------------------------------------------------------------------
/// Component.
//------------------------------------------------------------------------------
#[component(inline_props)]
pub fn Layout<G: Html>( cx: Scope, child: View<G> ) -> View<G>
{
    view!
    {
        cx,
        Header()
        (child)
        Footer()
    }
}

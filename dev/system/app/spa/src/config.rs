//------------------------------------------------------------------------------
//! Configuration.
//------------------------------------------------------------------------------


//------------------------------------------------------------------------------
/// Config.
//------------------------------------------------------------------------------
#[derive(Clone, Debug)]
pub(crate) struct Config
{
    pub(crate) api_url: String,
}

impl Config
{
    //--------------------------------------------------------------------------
    /// Initializes the configuration.
    //--------------------------------------------------------------------------
    pub(crate) fn init() -> Self
    {
        let api_url = std::env!("API_URL").to_string();

        Self
        {
            api_url,
        }
    }
}

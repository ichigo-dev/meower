//------------------------------------------------------------------------------
/// API schemas.
//------------------------------------------------------------------------------

pub mod mypage;

use serde::{ Serialize, Deserialize };


//------------------------------------------------------------------------------
/// API status code.
//------------------------------------------------------------------------------
#[derive(Debug, Serialize, Deserialize)]
pub enum ApiStatusCode
{
    Ok = 200,
    Ng = 400,
}


//------------------------------------------------------------------------------
/// API response.
//------------------------------------------------------------------------------
#[derive(Debug, Serialize, Deserialize)]
pub struct ApiResponse<T>
{
    pub code: ApiStatusCode,
    pub data: T,
}
